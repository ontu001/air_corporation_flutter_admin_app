  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = new Map<String, dynamic>();
    data['id'] = this.id;
    data['name'] = this.name;
    data['bg_color'] = this.bgColor;
    data['text_color'] = this.textColor;
    return data;
  }
}

